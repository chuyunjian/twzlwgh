        /// <param name="keyValue">主键</param>
        /// <summary>
        /// <returns></returns>
        public SMS_SendEntity GetEntity(string keyValue)
        {
            try
            {
                return this.BaseRepository().FindEntity<SMS_SendEntity>(keyValue);
            }
            catch (Exception ex)
            {
                if (ex is ExceptionEx)
                {
                    throw;
                }
                else
                {
